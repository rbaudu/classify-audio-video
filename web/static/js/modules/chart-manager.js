/**
 * Module de gestion des graphiques statistiques
 * CrÃ©e et met Ã  jour les diffÃ©rents graphiques Ã  partir des donnÃ©es
 */

const ChartManager = {
    // Ã‰tat du gestionnaire de graphiques
    state: {
        charts: {
            distribution: null,
            duration: null,
            hourly: null,
            trends: null
        },
        colors: null,
        currentPeriod: 'day'
    },

    /**
     * Initialise le gestionnaire de graphiques
     * @param {Object} colors - Couleurs associÃ©es aux activitÃ©s
     */
    init: function(colors) {
        this.state.colors = colors || CONFIG.ACTIVITY_COLORS;
    },
    
    /**
     * DÃ©finit la pÃ©riode actuelle
     * @param {string} period - PÃ©riode actuelle ('day', 'week', 'month', 'year')
     */
    setPeriod: function(period) {
        this.state.currentPeriod = period;
    },
    
    /**
     * Met Ã  jour tous les graphiques avec les donnÃ©es
     * @param {Object} data - DonnÃ©es de statistiques
     */
    updateCharts: function(data) {
        if (!data) return;
        
        this.createDistributionChart(data);
        this.createDurationChart(data);
        this.createHourlyDistributionChart(data);
        this.createTrendsChart(data);
    },
    
    /**
     * Nettoie tous les graphiques
     */
    clearCharts: function() {
        for (const key in this.state.charts) {
            if (this.state.charts[key]) {
                this.state.charts[key].destroy();
                this.state.charts[key] = null;
            }
        }
    },
    
    /**
     * CrÃ©e le graphique de rÃ©partition des activitÃ©s
     * @param {Object} data - DonnÃ©es de statistiques
     */
    createDistributionChart: function(data) {
        const canvas = document.getElementById('activity-distribution-chart');
        if (!canvas) return;
        
        // RÃ©cupÃ©rer les donnÃ©es de rÃ©partition
        const activities = Object.keys(data.activity_counts || {});
        const counts = activities.map(activity => data.activity_counts[activity] || 0);
        
        // Nettoyer le canvas si un graphique existe dÃ©jÃ 
        if (this.state.charts.distribution) {
            this.state.charts.distribution.destroy();
        }
        
        // CrÃ©er le graphique
        this.state.charts.distribution = new Chart(canvas, {
            type: 'pie',
            data: {
                labels: activities,
                datasets: [{
                    data: counts,
                    backgroundColor: activities.map(activity => this.state.colors[activity] || '#cccccc'),
                    borderWidth: 1
                }]
            },
            options: {
                responsive: true,
                maintainAspectRatio: false,
                plugins: {
                    legend: {
                        position: 'bottom',
                        labels: {
                            font: {
                                size: 12
                            },
                            generateLabels: function(chart) {
                                const labels = Chart.defaults.plugins.legend.labels.generateLabels(chart);
                                labels.forEach((label, i) => {
                                    const activity = activities[i];
                                    const icon = Utils.getActivityIcon ? Utils.getActivityIcon(activity) : 'ðŸ“Š';
                                    label.text = `${icon} ${label.text}`;
                                });
                                return labels;
                            }
                        }
                    },
                    tooltip: {
                        callbacks: {
                            label: function(context) {
                                const label = context.label || '';
                                const value = context.raw || 0;
                                const total = counts.reduce((a, b) => a + b, 0);
                                const percentage = Math.round((value / total) * 100);
                                return `${label}: ${value} (${percentage}%)`;
                            }
                        }
                    }
                }
            }
        });
    },
    
    /**
     * CrÃ©e le graphique de durÃ©e par activitÃ©
     * @param {Object} data - DonnÃ©es de statistiques
     */
    createDurationChart: function(data) {
        const canvas = document.getElementById('activity-duration-chart');
        if (!canvas) return;
        
        // RÃ©cupÃ©rer les donnÃ©es de durÃ©e
        const activities = Object.keys(data.activity_durations || {});
        const durations = activities.map(activity => {
            // Convertir en minutes pour une meilleure lisibilitÃ©
            return Math.round((data.activity_durations[activity] || 0) / 60);
        });
        
        // Nettoyer le canvas si un graphique existe dÃ©jÃ 
        if (this.state.charts.duration) {
            this.state.charts.duration.destroy();
        }
        
        // CrÃ©er le graphique
        this.state.charts.duration = new Chart(canvas, {
            type: 'bar',
            data: {
                labels: activities,
                datasets: [{
                    label: 'DurÃ©e (minutes)',
                    data: durations,
                    backgroundColor: activities.map(activity => this.state.colors[activity] || '#cccccc'),
                    borderWidth: 1
                }]
            },
            options: {
                responsive: true,
                maintainAspectRatio: false,
                indexAxis: 'y',
                plugins: {
                    legend: {
                        display: false
                    },
                    tooltip: {
                        callbacks: {
                            label: function(context) {
                                const minutes = context.raw || 0;
                                const hours = Math.floor(minutes / 60);
                                const mins = minutes % 60;
                                return `DurÃ©e: ${hours}h ${mins}m`;
                            }
                        }
                    }
                },
                scales: {
                    x: {
                        title: {
                            display: true,
                            text: 'Minutes'
                        }
                    },
                    y: {
                        ticks: {
                            callback: function(value, index) {
                                const activity = activities[index];
                                const icon = Utils.getActivityIcon ? Utils.getActivityIcon(activity) : 'ðŸ“Š';
                                return `${icon} ${activity}`;
                            }
                        }
                    }
                }
            }
        });
    },
    
    /**
     * CrÃ©e le graphique de distribution horaire
     * @param {Object} data - DonnÃ©es de statistiques
     */
    createHourlyDistributionChart: function(data) {
        const canvas = document.getElementById('hourly-distribution-chart');
        if (!canvas) return;
        
        // Nettoyer le canvas si un graphique existe dÃ©jÃ 
        if (this.state.charts.hourly) {
            this.state.charts.hourly.destroy();
        }
        
        // RÃ©cupÃ©rer les donnÃ©es de distribution horaire
        const hourly = data.hourly_distribution || [];
        
        // PrÃ©parer les donnÃ©es pour le graphique
        const hours = Array.from({ length: 24 }, (_, i) => i);
        const activities = Object.keys(this.state.colors);
        
        const datasets = activities.map(activity => {
            return {
                label: activity,
                data: hours.map(hour => {
                    const hourData = hourly.find(h => h.hour === hour) || { activities: {} };
                    return hourData.activities[activity] || 0;
                }),
                backgroundColor: this.state.colors[activity] || '#cccccc',
                borderWidth: 1
            };
        });
        
        // CrÃ©er le graphique
        this.state.charts.hourly = new Chart(canvas, {
            type: 'bar',
            data: {
                labels: hours.map(hour => `${hour}h`),
                datasets: datasets
            },
            options: {
                responsive: true,
                maintainAspectRatio: false,
                plugins: {
                    legend: {
                        position: 'bottom',
                        labels: {
                            font: {
                                size: 12
                            },
                            generateLabels: function(chart) {
                                const labels = Chart.defaults.plugins.legend.labels.generateLabels(chart);
                                labels.forEach((label, i) => {
                                    const activity = activities[i];
                                    const icon = Utils.getActivityIcon ? Utils.getActivityIcon(activity) : 'ðŸ“Š';
                                    label.text = `${icon} ${label.text}`;
                                });
                                return labels;
                            }
                        }
                    }
                },
                scales: {
                    x: {
                        title: {
                            display: true,
                            text: 'Heure de la journÃ©e'
                        },
                        stacked: true
                    },
                    y: {
                        title: {
                            display: true,
                            text: 'Occurrences'
                        },
                        stacked: true
                    }
                }
            }
        });
    },
    
    /**
     * CrÃ©e le graphique de tendances d'activitÃ©
     * @param {Object} data - DonnÃ©es de statistiques
     */
    createTrendsChart: function(data) {
        const canvas = document.getElementById('activity-trends-chart');
        if (!canvas) return;
        
        // Nettoyer le canvas si un graphique existe dÃ©jÃ 
        if (this.state.charts.trends) {
            this.state.charts.trends.destroy();
        }
        
        // RÃ©cupÃ©rer les donnÃ©es de tendances
        const trends = data.trends || [];
        
        // PrÃ©parer les donnÃ©es pour le graphique
        const timePoints = trends.map(t => t.date);
        const activities = Object.keys(this.state.colors);
        
        const datasets = activities.map(activity => {
            return {
                label: activity,
                data: trends.map(t => t.activities[activity] || 0),
                borderColor: this.state.colors[activity] || '#cccccc',
                backgroundColor: 'transparent',
                tension: 0.1
            };
        });
        
        // CrÃ©er le graphique
        this.state.charts.trends = new Chart(canvas, {
            type: 'line',
            data: {
                labels: timePoints,
                datasets: datasets
            },
            options: {
                responsive: true,
                maintainAspectRatio: false,
                plugins: {
                    legend: {
                        position: 'bottom',
                        labels: {
                            font: {
                                size: 12
                            },
                            generateLabels: function(chart) {
                                const labels = Chart.defaults.plugins.legend.labels.generateLabels(chart);
                                labels.forEach((label, i) => {
                                    const activity = activities[i];
                                    const icon = Utils.getActivityIcon ? Utils.getActivityIcon(activity) : 'ðŸ“Š';
                                    label.text = `${icon} ${label.text}`;
                                });
                                return labels;
                            }
                        }
                    }
                },
                scales: {
                    x: {
                        title: {
                            display: true,
                            text: this.getPeriodTimeLabel()
                        }
                    },
                    y: {
                        title: {
                            display: true,
                            text: 'DurÃ©e (minutes)'
                        }
                    }
                }
            }
        });
    },
    
    /**
     * Obtient le libellÃ© de l'axe X en fonction de la pÃ©riode
     * @returns {string} LibellÃ© de l'axe X
     */
    getPeriodTimeLabel: function() {
        switch (this.state.currentPeriod) {
            case 'day':
                return 'Heures';
            case 'week':
                return 'Jours';
            case 'month':
                return 'Jours du mois';
            case 'year':
                return 'Mois';
            default:
                return 'Temps';
        }
    },
    
    /**
     * Affiche un message de chargement sur tous les graphiques
     */
    showLoading: function() {
        const chartContainers = document.querySelectorAll('.chart-container');
        chartContainers.forEach(container => {
            container.innerHTML = '<div class="loading-indicator">Chargement des donnÃ©es...</div>';
        });
    },
    
    /**
     * Affiche un message d'erreur sur tous les graphiques
     */
    showError: function() {
        const chartContainers = document.querySelectorAll('.chart-container');
        chartContainers.forEach(container => {
            container.innerHTML = '<div class="error-message">Erreur lors du chargement des donnÃ©es</div>';
        });
    }
};

// Exporter le module
window.ChartManager = ChartManager;
